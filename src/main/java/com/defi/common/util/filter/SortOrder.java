package com.defi.common.util.filter;

/**
 * Enumeration representing database sort order directions.
 * 
 * <p>
 * This enum provides constants for specifying sort order in database
 * queries and other ordering operations.
 * </p>
 * 
 * @author Defi Team
 * @since 1.0.5
 */
public enum SortOrder {

    /**
     * Ascending sort order (lowest to highest).
     */
    ASC,

    /**
     * Descending sort order (highest to lowest).
     */
    DESC;
}
